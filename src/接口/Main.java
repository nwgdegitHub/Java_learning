/*
* 抽象类 和 接口的区别？
* 1。设计理念的区别
*       抽象类中定义的是该继承体系的共性功能
*       接口中定义的是该继承体系的扩展功能
* 2。关系区别
*       类与类，只能是单继承
*       类与接口，可以是单实现，也可以是多实现
*       接口与接口， 可以是多继承
* 3。成员区别
*       在抽象类中：
*           成员变量：可以是常量，也可以是变量
*           构造方法：有
*           成员方法：可以抽象，也可以非抽象
*       在接口中：
*           成员变量：只能常量
*           成员方法：只能抽象
* */

